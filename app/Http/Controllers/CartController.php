    //        dd($request->all());
        $cart_product = Product::where('id',$request->pro_id)->first();

        Cart::add([
            'id'=>$cart_product->id,
            'name'=>$cart_product->productName,
            'price'=>$cart_product->price,
            'qty'=>$cart_product->qty,
        ]);

        return redirect()->route('cart_show');
